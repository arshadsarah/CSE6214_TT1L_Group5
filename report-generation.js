const API_URL = "admin_reports.php";
const NOTIFICATION_API_URL = "admin_notifications.php";

const reportForm = document.getElementById("reportForm");
const reportType = document.getElementById("reportType");
const startDate = document.getElementById("startDate");
const endDate = document.getElementById("endDate");
const reportStatus = document.getElementById("reportStatus");
const generatedBy = document.getElementById("generatedBy");

const clearFormBtn = document.getElementById("clearFormBtn");
const searchInput = document.getElementById("searchInput");
const reportTableBody = document.getElementById("reportTableBody");

const reportModal = document.getElementById("reportModal");
const closeModal = document.getElementById("closeModal");
const reportDetails = document.getElementById("reportDetails");

const dropdownBtn = document.getElementById("dropdownBtn");
const dropdownMenu = document.getElementById("dropdownMenu");
const adminDropdown = document.getElementById("adminDropdown");

let reports = [];

/* Admin dropdown */
if (dropdownBtn && dropdownMenu) {
  dropdownBtn.addEventListener("click", (event) => {
    event.stopPropagation();
    dropdownMenu.classList.toggle("show");
  });
}

/* Close dropdown when clicking outside */
document.addEventListener("click", (event) => {
  if (
    adminDropdown &&
    dropdownMenu &&
    !adminDropdown.contains(event.target)
  ) {
    dropdownMenu.classList.remove("show");
  }
});

function escapeHTML(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function formatReportId(id) {
  return "R" + String(id).padStart(3, "0");
}

function setError(input, errorId, message) {
  input.classList.add("invalid");
  document.getElementById(errorId).textContent = message;
}

function clearError(input, errorId) {
  input.classList.remove("invalid");
  document.getElementById(errorId).textContent = "";
}

function clearErrors() {
  clearError(reportType, "reportTypeError");
  clearError(startDate, "startDateError");
  clearError(endDate, "endDateError");
  clearError(reportStatus, "reportStatusError");
  clearError(generatedBy, "generatedByError");
}

function validateForm() {
  clearErrors();

  let isValid = true;

  if (reportType.value === "") {
    setError(reportType, "reportTypeError", "Please select report type.");
    isValid = false;
  }

  if (startDate.value === "") {
    setError(startDate, "startDateError", "Start date is required.");
    isValid = false;
  }

  if (endDate.value === "") {
    setError(endDate, "endDateError", "End date is required.");
    isValid = false;
  }

  if (startDate.value && endDate.value && endDate.value < startDate.value) {
    setError(endDate, "endDateError", "End date cannot be before start date.");
    isValid = false;
  }

  if (reportStatus.value === "") {
    setError(reportStatus, "reportStatusError", "Please select status.");
    isValid = false;
  }

  const generatedByValue = generatedBy.value.trim();

  if (generatedByValue === "") {
    setError(generatedBy, "generatedByError", "Generated by is required.");
    isValid = false;
  } else if (generatedByValue.length < 3) {
    setError(generatedBy, "generatedByError", "Enter at least 3 characters.");
    isValid = false;
  } else if (!/^[A-Za-z .'-]+$/.test(generatedByValue)) {
    setError(
      generatedBy,
      "generatedByError",
      "Only letters, spaces, dot, apostrophe and dash are allowed."
    );
    isValid = false;
  }

  return isValid;
}

/* Load reports from PHP */
function loadReports() {
  fetch(API_URL)
    .then((response) => response.json())
    .then((data) => {
      if (data.success) {
        reports = data.reports || [];
        renderReports(searchInput.value.trim());
        updateStats();
      } else {
        showToast(data.message || "Failed to load reports.");
      }
    })
    .catch((error) => {
      console.error("Load reports error:", error);
      showToast("Backend connection failed.");
    });
}

function renderReports(filterText = "") {
  reportTableBody.innerHTML = "";

  const filteredReports = reports.filter((report) => {
    const text = `
      ${report.reportID}
      ${report.reportType}
      ${report.startDate}
      ${report.endDate}
      ${report.generatedBy}
      ${report.status}
      ${report.createdAt}
    `.toLowerCase();

    return text.includes(filterText.toLowerCase());
  });

  if (filteredReports.length === 0) {
    reportTableBody.innerHTML = `
      <tr>
        <td colspan="6" style="text-align:center;">No reports found</td>
      </tr>
    `;

    updateStats();
    return;
  }

  filteredReports.forEach((report) => {
    const row = document.createElement("tr");

    row.innerHTML = `
      <td>${formatReportId(report.reportID)}</td>
      <td>${escapeHTML(report.reportType)}</td>
      <td>${escapeHTML(report.startDate)} to ${escapeHTML(report.endDate)}</td>
      <td>${escapeHTML(report.generatedBy)}</td>
      <td><span class="status ${escapeHTML(report.status)}">${escapeHTML(report.status)}</span></td>
      <td>
        <button class="action-btn view-btn" onclick="viewReport(${report.reportID})">View</button>
        <button class="action-btn download-btn" onclick="downloadReport(${report.reportID})">Download</button>
        <button class="action-btn delete-btn" onclick="deleteReport(${report.reportID})">Delete</button>
      </td>
    `;

    reportTableBody.appendChild(row);
  });

  updateStats();
}

function updateStats() {
  const totalReports = document.getElementById("totalReports");
  const generatedReports = document.getElementById("generatedReports");
  const pendingReports = document.getElementById("pendingReports");
  const totalBookings = document.getElementById("totalBookings");

  const generatedCount = reports.filter((report) => report.status === "Generated").length;
  const pendingCount = reports.filter((report) => report.status === "Pending").length;

  totalReports.textContent = reports.length;
  generatedReports.textContent = generatedCount;
  pendingReports.textContent = pendingCount;

  /* This will show 0 unless your PHP later sends booking count */
  totalBookings.textContent = 0;
}

/* Submit report form */
reportForm.addEventListener("submit", (event) => {
  event.preventDefault();

  if (!validateForm()) {
    showToast("Invalid input. Please fix the form.");
    return;
  }

  const reportData = {
    reportType: reportType.value,
    startDate: startDate.value,
    endDate: endDate.value,
    status: reportStatus.value,
    generatedBy: generatedBy.value.trim()
  };

  fetch(API_URL, {
    method: "POST",
    headers: {
      "Content-Type": "application/json"
    },
    body: JSON.stringify(reportData)
  })
    .then((response) => response.json())
    .then((data) => {
      if (data.success) {
        reportForm.reset();
        clearErrors();
        showToast(data.message || "Report generated successfully.");
        loadReports();
      } else {
        showToast(data.message || "Failed to generate report.");
      }
    })
    .catch((error) => {
      console.error("Create report error:", error);
      showToast("Backend connection failed.");
    });
});

clearFormBtn.addEventListener("click", () => {
  reportForm.reset();
  clearErrors();
  showToast("Form cleared.");
});

searchInput.addEventListener("input", () => {
  renderReports(searchInput.value.trim());
});

function viewReport(id) {
  const report = reports.find((item) => {
    return Number(item.reportID) === Number(id);
  });

  if (!report) return;

  reportDetails.innerHTML = `
    <div class="detail-row"><strong>Report ID:</strong> ${formatReportId(report.reportID)}</div>
    <div class="detail-row"><strong>Report Type:</strong> ${escapeHTML(report.reportType)}</div>
    <div class="detail-row"><strong>Date Range:</strong> ${escapeHTML(report.startDate)} to ${escapeHTML(report.endDate)}</div>
    <div class="detail-row"><strong>Generated By:</strong> ${escapeHTML(report.generatedBy)}</div>
    <div class="detail-row"><strong>Status:</strong> ${escapeHTML(report.status)}</div>
    <div class="detail-row"><strong>Created At:</strong> ${escapeHTML(report.createdAt || "Not available")}</div>
  `;

  reportModal.classList.add("show");
}

closeModal.addEventListener("click", () => {
  reportModal.classList.remove("show");
});

reportModal.addEventListener("click", (event) => {
  if (event.target === reportModal) {
    reportModal.classList.remove("show");
  }
});

function downloadReport(id) {
  const report = reports.find((item) => {
    return Number(item.reportID) === Number(id);
  });

  if (!report) return;

  const csvContent =
    "Report ID,Report Type,Start Date,End Date,Generated By,Status,Created At\n" +
    `${formatReportId(report.reportID)},${report.reportType},${report.startDate},${report.endDate},${report.generatedBy},${report.status},${report.createdAt || ""}`;

  const blob = new Blob([csvContent], { type: "text/csv" });
  const downloadLink = document.createElement("a");

  downloadLink.href = URL.createObjectURL(blob);
  downloadLink.download = `${formatReportId(report.reportID)}-${report.reportType.replaceAll(" ", "-")}.csv`;

  document.body.appendChild(downloadLink);
  downloadLink.click();
  document.body.removeChild(downloadLink);

  showToast("Report downloaded.");
}

function deleteReport(id) {
  const confirmDelete = confirm("Are you sure you want to delete this report?");

  if (!confirmDelete) {
    return;
  }

  fetch(`${API_URL}?id=${id}`, {
    method: "DELETE"
  })
    .then((response) => response.json())
    .then((data) => {
      if (data.success) {
        showToast(data.message || "Report deleted successfully.");
        loadReports();
      } else {
        showToast(data.message || "Failed to delete report.");
      }
    })
    .catch((error) => {
      console.error("Delete report error:", error);
      showToast("Backend connection failed.");
    });
}

/* Notification Bell */
function openNotificationPage() {
  window.location.href = "notification-management.html";
}

function updateNotificationDot() {
  const notificationDot = document.getElementById("notificationDot");

  if (!notificationDot) return;

  fetch(NOTIFICATION_API_URL)
    .then((response) => response.json())
    .then((data) => {
      if (!data.success) return;

      const notifications = data.notifications || [];

      const hasPendingNotification = notifications.some((item) => {
        return item.status === "Draft" || item.status === "Scheduled";
      });

      notificationDot.style.display = hasPendingNotification ? "block" : "none";
    })
    .catch((error) => {
      console.error("Notification dot error:", error);
      notificationDot.style.display = "none";
    });
}

function showToast(message) {
  const toast = document.getElementById("toast");

  if (!toast) return;

  toast.textContent = message;
  toast.classList.add("show");

  setTimeout(() => {
    toast.classList.remove("show");
  }, 2300);
}

[reportType, startDate, endDate, reportStatus, generatedBy].forEach((input) => {
  input.addEventListener("input", clearErrors);
  input.addEventListener("change", clearErrors);
});

/* Load data */
loadReports();
updateNotificationDot();